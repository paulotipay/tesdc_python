# this variable will hold an integer
number = 10
# display the value
# first way
print("The number is " + str(number))
# second way
print(f'The number is {number}')

# this variable will hold a string
name = "Peter"
# display the value
print(f'The name is {name}')

# this variable will hold a float
floatingNumber = 2.0
# display the value
print(f'The floating value is {floatingNumber}')

# this variable will hold a boolean
isValid = True
# display the value
print(f'The boolean value is {isValid}')
