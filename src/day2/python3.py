# changing variable value and checking data type
# create a variable and assign 10
x = 10
# check the data type
print(type(x))

# reassign value from 10 to Hello
x = "Hello"
print(type(x))

# reassign value from "Hello" to Boolean True
x = True
print(type(x))

# reassign value from Boolean True to float 3.14
x = 3.14
print(type(x))